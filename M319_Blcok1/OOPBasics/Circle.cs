using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOPBasics
{
    class Circle
    {
        //Membervariablen
        public double _diameter;


        //Konstuktoren

        //Methoden

        public double CircleArea()
        {
            return Math.Pow(_diameter, 2) * Math.PI / 4;
        }


    }
